import os
import sys


def removeDuplicate(out):
    newOut = []
    for i in out:
        if i in newOut:
            #print ("%d Already available" %i)
            continue
        else:
            newOut.append(i)
    return newOut
out = [1,3,4,4,2,5,2,3]
print (removeDuplicate(out))
